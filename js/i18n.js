  function getCurrentLang() {
    return document.documentElement.getAttribute('lang') || 'en';
  }

  function t(key) {
    const lang = getCurrentLang();
    const table = translations[lang] || translations.en;
    return table[key] || translations.en[key] || key;
  }

  function applyTranslations(root = document) {
    // Text content
    root.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = t(key);
    });
    // Attributes (data-i18n-attr="placeholder:key")
    root.querySelectorAll('[data-i18n-attr]').forEach(el => {
      const parts = el.getAttribute('data-i18n-attr').split(',');
      parts.forEach(p => {
        const [attr, k] = p.split(':').map(s => s.trim());
        if (attr && k) el.setAttribute(attr, t(k));
      });
    });
  }

  // Expose globally
  window.__i18n = { t, applyTranslations, translations };
